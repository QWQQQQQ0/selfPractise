    </el-form-item>
</template>

<script>
export default {
    inject: {
        handleDialog: {
            default: () =>(() =>{})
        }
    },
    props: {
        preInfo: {
            type: Object,
            required: true,
        },
        bindValues: {
            type: Object,
            require: true
        },
        intable: {
            type: Boolean,
            default: () => false
        }
    },
    data() {
        return {

        }
    },
    methods: {
      handleSelectDialog(type, item) {
        if(this.handleDialog) {
            this.handleDialog(item,this.bindValues[item.model])
        }

      }
    }
}
</script>

